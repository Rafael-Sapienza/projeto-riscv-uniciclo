library IEEE;
use IEEE.STD_LOGIC_1164.ALL;

entity ALUControl is
    Port (
        ALUOp      : in  STD_LOGIC_VECTOR(1 downto 0);
        funct3     : in  STD_LOGIC_VECTOR(2 downto 0);
        funct7     : in  STD_LOGIC_VECTOR(6 downto 0);
        ALUControl : out STD_LOGIC_VECTOR(3 downto 0)
    );
end ALUControl;

architecture Behavioral of ALUControl is
begin
process(ALUOp, funct3, funct7)
begin
    case ALUOp is
        ------------------------------------------------------------------
        -- ADD
        ------------------------------------------------------------------
        when "00" =>
            ALUControl <= "0000";
        ------------------------------------------------------------------
        -- SUB (BEQ/BNE)
        ------------------------------------------------------------------
        when "01" =>
            ALUControl <= "0001";
        ------------------------------------------------------------------
        -- Tipo R e Tipo I
        ------------------------------------------------------------------
        when "10" =>
            case funct3 is
                ----------------------------------------------------------
                -- ADD / SUB / ADDI
                ----------------------------------------------------------
                when "000" =>
                    if funct7 = "0100000" then
                        ALUControl <= "0001";      -- SUB
                    else
                        ALUControl <= "0000";      -- ADD
                    end if;
                ----------------------------------------------------------
                -- SLL / SLLI
                ----------------------------------------------------------
                when "001" =>
                    ALUControl <= "0101";
                ----------------------------------------------------------
                -- SLT / SLTI  (Set Less Than, com sinal)
                ----------------------------------------------------------
                when "010" =>
                    ALUControl <= "1000";      -- uSLT
                ----------------------------------------------------------
                -- SLTU / SLTIU  (Set Less Than, sem sinal)
                ----------------------------------------------------------
                when "011" =>
                    ALUControl <= "1001";      -- uSLTU
                ----------------------------------------------------------
                -- XOR / XORI
                ----------------------------------------------------------
                when "100" =>
                    ALUControl <= "0100";
                ----------------------------------------------------------
                -- SRL / SRA / SRLI / SRAI
                ----------------------------------------------------------
                when "101" =>
                    if funct7 = "0100000" then
                        ALUControl <= "0111";      -- SRA
                    else
                        ALUControl <= "0110";      -- SRL
                    end if;
                ----------------------------------------------------------
                -- OR / ORI
                ----------------------------------------------------------
                when "110" =>
                    ALUControl <= "0011";
                ----------------------------------------------------------
                -- AND / ANDI
                ----------------------------------------------------------
                when "111" =>
                    ALUControl <= "0010";
                when others =>
                    ALUControl <= "0000";
            end case;
        when others =>
            ALUControl <= "0000";
    end case;
end process;
end Behavioral;